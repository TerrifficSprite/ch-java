package org.example.hibernate.DTO;

public class ChatDTO {

    private String title;

    private String code;
    public ChatDTO() {
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

}
